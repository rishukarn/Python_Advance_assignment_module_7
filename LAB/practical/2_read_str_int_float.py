# Write a Python program to read a string, an integer, and a float from
# the keyboard and display them.

# Read a string from the user
string_value=input('Enter string value: ')

# Read a integer from the user
integer_value=int(input('Enter integer value: '))

# Read a float from the user
float_value=int(input('Enter integer value: '))

#Display the value
print("String entered:", string_value)
print("Integer entered:", integer_value)
print("Float entered:", float_value)
